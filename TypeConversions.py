# Integer
i = 2
print(type(i))

# Floating point
f = 2.71828
print(type(f))

# Complex no.
c = 0.5 + 1j
print(type(c))

# Type conversion
print(float(i), '&', int(f), '&', c)

# Boolean
a = 2 > 1
print(a)
print(type(a))

# String
znak = 'A'
print(znak)
print(type(znak))

napis = "Ala ma kota"
print(napis[0])

# HERE IMPORTANT, HAS NOT ONLY START & STOP BUT ALSO STEP!!!
print(napis[0::2])